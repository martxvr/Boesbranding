<script>
function showProject(index) {
    const projects = document.querySelectorAll('.p1-afgelopen-reizen');
    projects.forEach((project, idx) => {
        if (idx === index) {
            project.classList.add('active');
        } else {
            project.classList.remove('active');
        }
    });

    const buttons = document.querySelectorAll('.p-boes-button-blue');
    buttons.forEach((button, idx) => {
        if (idx === index) {
            button.classList.add('active');
            button.style.opacity = '1'; // Zet de actieve knop op 100% opacity
        } else {
            button.classList.remove('active');
            button.style.opacity = '0.33'; // Zet de niet-actieve knoppen op 33% opacity
        }
    });
}

window.onload = function() {
    showProject(0); // Toon het eerste project en markeer de eerste knop als actief
};
</script>

<script>
    let currentReiziger = 1;
const totalReizigers = 5; // Aantal reizigers

// Functie voor het wisselen naar de volgende reiziger
function nextReiziger() {
    document.querySelector(`.reiziger${currentReiziger}`).classList.remove('active');
    currentReiziger = currentReiziger === totalReizigers ? 1 : currentReiziger + 1;
    document.querySelector(`.reiziger${currentReiziger}`).classList.add('active');
}

// Functie voor het wisselen naar de vorige reiziger
function prevReiziger() {
    document.querySelector(`.reiziger${currentReiziger}`).classList.remove('active');
    currentReiziger = currentReiziger === 1 ? totalReizigers : currentReiziger - 1;
    document.querySelector(`.reiziger${currentReiziger}`).classList.add('active');
}
</script>

<script>
    document.addEventListener("DOMContentLoaded", function () {
    // Select all fase cards
    const faseCards = document.querySelectorAll(".fase-card");

    // Function to close all fases
    function closeAllFases() {
        faseCards.forEach(card => {
            card.classList.remove("active");
        });
    }

    // Add click event to each fase
    faseCards.forEach(card => {
        card.addEventListener("click", function () {
            // Close all other fases
            closeAllFases();
            // Open clicked fase
            this.classList.add("active");
        });
    });

    // Set Fase 1 as active by default
    document.getElementById("fase1").classList.add("active");
});
</script>

